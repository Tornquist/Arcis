package com.petronicarts.arcis;

public class Node {

	private int x;			
	private int y;			
	private int parentX;
	private int parentY;
	private int cost;
	private int direction;
	private int open;

	public Node(int X, int Y) {
		this.x = X;
		this.y = Y;
		this.open = 2;
		this.parentX = -1;
		this.parentY = -1;
		this.cost = -1;
		this.direction = 0;
		
	}

	public int getOpen() {
		return open;
	}
	
	public void setOpen(int Open) {
		this.open = Open;
	}
	
	public int getDirection() {
		return direction;
	}
	
	public void setDirection(int Direction) {
		this.direction = Direction;
	}
	
	public int getCost() {
		return cost;
	}
	public void setCost(int Cost) {
		this.cost = Cost;
	}
	
	public int getParentY() {
		return parentY;
	}
	public void setParentY(int Y) {
		this.parentY= Y;
	}
	
	public int getParentX() {
		return parentX;
	}
	public void setParentX(int X) {
		this.parentX= X;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int X) {
		this.x = X;
	}
	
	public int getY() {
		return y;
	}
	public void setY(int Y) {
		this.y = Y;
	}
	
}
